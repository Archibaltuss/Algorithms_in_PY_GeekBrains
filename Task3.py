# По введенным пользователем координатам двух точек
# вывести уравнение прямой вида y = kx + b, проходящей через эти точки.

print('Введите координаты (х1, у1) (х2, у2)')
x1 = float(input('x1 = '))
y1 = float(input('x1 = '))
x2 = float(input('x1 = '))
y2 = float(input('x1 = '))

k = (y1 - y2) // (x1 - x2)
b = y2 - k * x2

print(f'y = {k: .3f} *x + {b: .3f}')

